RJF.renderLegalPage = function(key){
  var root = document.getElementById('legal-root');
  if(!root) return;
  var c = RJF.legalContent[key];
  if(!c) return;

  var sectionsHtml = c.sections.map(function(s){
    return '<h2>' + s.h + '</h2><p>' + s.p + '</p>';
  }).join('');

  root.innerHTML =
    '<div class="legal-page">' +
      '<a class="legal-back" href="#/">' + RJF.iconSvg('up', 'fill="none" stroke="currentColor" stroke-width="2" style="transform:rotate(-90deg)"') + '<span>মূল পাতায় ফিরুন</span></a>' +
      '<h1>' + c.title + '</h1>' +
      '<span class="legal-updated">' + c.updated + '</span>' +
      sectionsHtml +
    '</div>';
};
